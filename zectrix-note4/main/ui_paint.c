// ---- Framebuffer helpers ---------------------------------------------------
void ui_init(void) {
    if (!s_fb) s_fb = heap_caps_malloc(EPD_FRAMEBUF_BYTES, MALLOC_CAP_8BIT);
    if (s_fb) memset(s_fb, 0xFF, EPD_FRAMEBUF_BYTES);
}

uint8_t *ui_framebuf(void) { return s_fb; }

void ui_clear(bool white) {
    if (!s_fb) return;
    memset(s_fb, white ? 0xFF : 0x00, EPD_FRAMEBUF_BYTES);
}

// In 1bpp framebuffer, bit is MSB-first within each byte.
void ui_pixel(int x, int y, bool white) {
    if (!s_fb) return;
    if (x < 0 || x >= 400 || y < 0 || y >= 300) return;
    int byte_idx = y * EPD_1BPP_STRIDE + (x >> 3);
    uint8_t mask = 0x80 >> (x & 7);
    if (white) s_fb[byte_idx] |= mask;
    else       s_fb[byte_idx] &= ~mask;
}

void ui_hline(int x, int y, int w, bool white) {
    for (int i = 0; i < w; i++) ui_pixel(x + i, y, white);
}
void ui_vline(int x, int y, int h, bool white) {
    for (int i = 0; i < h; i++) ui_pixel(x, y + i, white);
}
void ui_fill(int x, int y, int w, int h, bool white) {
    for (int j = 0; j < h; j++)
        for (int i = 0; i < w; i++)
            ui_pixel(x + i, y + j, white);
}
void ui_rect(int x, int y, int w, int h, bool white) {
    ui_hline(x, y, w, white);
    ui_hline(x, y + h - 1, w, white);
    ui_vline(x, y, h, white);
    ui_vline(x + w - 1, y, h, white);
}
void ui_frame(int x, int y, int w, int h, int t) {
    for (int i = 0; i < t; i++)
        ui_rect(x + i, y + i, w - 2 * i, h - 2 * i, false);
}

// ---- Text ------------------------------------------------------------------
static void draw_glyph(int x, int y, char c, int scale) {
    if (c < 0x20 || c > 0x7F) c = '?';
    const uint8_t *g = font5x7[c - 0x20];
    for (int col = 0; col < 5; col++) {
        uint8_t bits = g[col];
        for (int row = 0; row < 7; row++) {
            if (bits & (1 << row)) {
                ui_fill(x + col * scale, y + row * scale, scale, scale, false);
            }
        }
    }
}

int ui_text(int x, int y, const char *s, int scale) {
    if (scale < 1) scale = 1;
    while (*s) {
        draw_glyph(x, y, *s++, scale);
        x += 6 * scale;   // 5 glyph + 1 space
    }
    return x;
}

int ui_text_width(const char *s, int scale) {
    int n = 0; while (s[n]) n++;
    return n * 6 * scale;
}

int ui_text_center(int cx, int y, const char *s, int scale) {
    int w = ui_text_width(s, scale);
    return ui_text(cx - w / 2, y, s, scale);
}

void ui_progress(int x, int y, int w, int h, int val, int max) {
    ui_rect(x, y, w, h, false);                    // border
    if (max <= 0 || val <= 0) return;
    if (val > max) val = max;
    int inner_w = w - 4;
    int filled = (inner_w * val) / max;
    ui_fill(x + 2, y + 2, filled, h - 4, false);
}
